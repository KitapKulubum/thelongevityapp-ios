    /// Gets the device's current language code (e.g., "tr", "en", "es")
    /// Returns "en" as default if device language is not supported
    func getDeviceLanguageCode() -> String {
        // Get preferred languages from device
        let preferredLanguage = Locale.preferredLanguages.first ?? "en"
        
        // Extract language code from "tr-TR" -> "tr"
        let languageCode = preferredLanguage.components(separatedBy: "-").first?.lowercased() ?? "en"
        
        // Check if supported, otherwise default to "en"
        if codeToLanguage.keys.contains(languageCode) {
            return languageCode
        }
        
        return "en"
    }
    
    /// Gets the language code for a given display name
    func getLanguageCode(for languageName: String) -> String {
        return languageToCode[languageName] ?? "en"
    }
    
    /// Gets the display name for a given language code
    func getLanguageName(for code: String) -> String {
        return codeToLanguage[code] ?? "English"
    }
    
    /// Gets the current language code
    var currentLanguageCode: String {
        return getLanguageCode(for: currentLanguage)
    }
    
    var currentLocale: Locale {
        guard let localeIdentifier = languageToLocale[currentLanguage] else {
            return Locale(identifier: "en_US")
        }
        return Locale(identifier: localeIdentifier)
    }
    
    /// Sets the language by display name
    func setLanguage(_ language: String) {
        currentLanguage = language
        storedLanguage = language
    }
    
    /// Sets the language by language code (e.g., "tr", "en")
    func setLanguageCode(_ code: String) {
        if let languageName = codeToLanguage[code] {
            setLanguage(languageName)
        } else {
            // Default to English if code not found
            setLanguage("English")
        }
    }
    
    func dateFormatter(format: String) -> DateFormatter {
        let formatter = DateFormatter()
        formatter.dateFormat = format
        formatter.locale = currentLocale
        formatter.timeZone = TimeZone.current
        return formatter
    }
    
    func monthFormatter(style: DateFormatter.Style = .short) -> DateFormatter {
        let formatter = DateFormatter()
        formatter.locale = currentLocale
        formatter.dateFormat = "MMM" // Abbreviated month
        return formatter
    }
    
